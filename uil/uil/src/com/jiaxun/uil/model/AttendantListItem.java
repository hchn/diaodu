package com.jiaxun.uil.model;

import com.jiaxun.sdk.dcl.model.Attendant;
import com.jiaxun.uil.model.BaseListItem;

/**
 * 说明：调度台本地操作用户实体
 * 
 * @author hubin
 * 
 * @Date 2015-2-10
 */
public class AttendantListItem extends BaseListItem
{
    private Attendant attendant;
    
    public AttendantListItem()
    {
        attendant = new Attendant();
    }

    public int getId()
    {
        return attendant.getId();
    }

    public void setId(int id)
    {
        this.attendant.setId(id);
    }

    public String getName()
    {
        return this.attendant.getName();
    }

    public void setName(String name)
    {
        this.attendant.setName(name);
    }

    public String getLogin()
    {
        return attendant.getLogin();
    }

    public void setLogin(String login)
    {
        this.attendant.setLogin(login);
    }

    public String getPassword()
    {
        return attendant.getPassword();
    }

    public void setPassword(String password)
    {
        this.attendant.setPassword(password);
    }

    public int getPriority()
    {
        return attendant.getPriority();
    }

    public void setPriority(int priority)
    {
        this.attendant.setPriority(priority);
    }
    
    public Attendant getAttendant()
    {
        return attendant;
    }

    public void setAttendant(Attendant attendant)
    {
        this.attendant = attendant;
    }

}
