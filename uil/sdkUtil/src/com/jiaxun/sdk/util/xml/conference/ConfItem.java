package com.jiaxun.sdk.util.xml.conference;

/**
 * 说明：会议消息item
 * 
 * @author fuluo
 * 
 * @Date 2015-4-29
 */
public class ConfItem
{
    public final static String TAG = "item";
    public final static String ATTRIBUTE_USERURI = "userURI";
    public final static String ATTRIBUTE_USERCALLID = "userCallID";
    public final static String TAG_ROLES = "roles";
    public final static String TAG_JOININGMETHOD = "joining-method";
    
    public final static String ROLES_ATTENDEE = "attendee";
    public final static String JOININGMETHOD_DIALED_OUT = "dialed-out";
    public final static String USERCALLID_ALL = "all";
    
    private String userUri;
    private String userCallID;
    private ConfCall call;
    private ConfMediaInfo mediaInfo = new ConfMediaInfo();
    private ConfTask task;
    private ConfToneInfo toneInfo;

    public String getUserUri()
    {
        return userUri;
    }

    public void setUserUri(String userUri)
    {
        this.userUri = userUri;
    }

    public String getUserCallID()
    {
        return userCallID;
    }

    public void setUserCallID(String userCallID)
    {
        this.userCallID = userCallID;
    }

    public ConfCall getCall()
    {
        return call;
    }

    public void setCall(ConfCall call)
    {
        this.call = call;
    }

    public ConfMediaInfo getMediaInfo()
    {
        return mediaInfo;
    }

    public void setMediaInfo(ConfMediaInfo mediaInfo)
    {
        this.mediaInfo = mediaInfo;
    }

    public ConfTask getTask()
    {
        return task;
    }

    public void setTask(ConfTask task)
    {
        this.task = task;
    }

    public ConfToneInfo getToneInfo()
    {
        return toneInfo;
    }

    public void setToneInfo(ConfToneInfo toneInfo)
    {
        this.toneInfo = toneInfo;
    }

}
